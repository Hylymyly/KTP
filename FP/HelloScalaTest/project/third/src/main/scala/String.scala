package exercise3
object Strings {


  def Uppercase(str: String): String = str.toUpperCase

  def testUppercase(str: String): String = Uppercase(str)

  def Interpolations(name: String, age: Int): String = {
    return "Hi my name is %s and I am %d years old.".format(name, age)
  }

  def testInterpolations(name: String, age: Int): String = Interpolations(name, age)

  def Computation(a: Int, b: Int): String = "Hi,\nnow follows a quite hard calculation. We try to add:\n\ta := %d\n\tb := %d\n\n\tresult is %d".format(a, b, a + b)

  def testComputation(a: Int, b: Int): String = Computation(a, b)

  def TakeTwo(str: String): String = {
    if (str.length == 2) return str
    else return str.take(2)
  }

  def testTaleTwo(str: String): String = TakeTwo(str)
}